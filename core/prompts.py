"""论文生成各阶段的 Prompt 模板"""

SYSTEM_PROMPT = """你是一位资深的学术论文写作专家，专注于社会科学、管理学、教育学领域的实证研究论文。
你的任务是帮助撰写高质量的毕业论文。

写作规范：
- 使用学术论文的正式语言，但避免过度晦涩
- 使用 Markdown 格式：## 表示一级标题，### 表示二级标题
- 段落之间用空行分隔
- 统计结果需要规范报告（如 t(98)=2.35, p<.05, d=0.47）
- 每个章节 2000-4000 字
- 引用标注用 [作者, 年份] 格式"""

OUTLINE_PROMPT = """根据以下开题报告内容，生成一份详细的论文大纲。

## 开题报告内容
{proposal}

## 数据概况
{data_summary}

请生成：
1. 论文完整标题
2. 关键词（5个）
3. 各章节标题和子标题（6-7章）
4. 每个章节的核心内容概要（2-3句话）
5. 研究假设（如有）
6. 统计方法建议"""

LITERATURE_PROMPT = """根据以下论文大纲和研究主题，撰写文献综述章节。

## 论文大纲
{outline}

## 研究主题
{topic}

要求：
- 分为 3-4 个子节：理论基础、核心概念界定、国内外研究现状、文献述评
- 每个子节 800-1200 字
- 引用标注用 [作者, 年份] 格式（标注[待核实]提醒后续补充真实文献）
- 结尾点明研究空白和本研究的切入点"""

METHODOLOGY_PROMPT = """根据以下信息撰写研究方法章节。

## 论文大纲
{outline}

## 数据概况
{data_summary}

要求：
- 包含：研究设计、研究对象、研究工具（问卷/量表）、数据收集过程、数据分析方法
- 说明选择该统计方法的理由
- 说明信效度检验方法
- 如有问卷，说明量表来源和修改情况"""

RESULTS_PROMPT = """根据以下数据分析结果，撰写数据分析与结果章节。

## 论文大纲
{outline}

## 数据分析结果
{analysis_results}

要求：
- 包含：描述性统计、信效度检验、假设检验结果
- 统计结果规范报告（均值、标准差、t值/F值、p值、效应量）
- 每个统计表后都要有文字解读
- 结果客观呈现，不在此章讨论原因"""

CONCLUSION_PROMPT = """根据以下论文各章节内容，撰写研究结论与讨论章节。

## 论文大纲
{outline}

## 研究结果摘要
{results_summary}

要求：
- 包含：主要研究发现、理论贡献、实践建议、研究局限、未来展望
- 结论要与前文假设对应
- 局限性要具体（样本量、方法、变量等），不要泛泛而谈
- 建议要有可操作性"""

ABSTRACT_PROMPT = """根据以下论文内容，撰写中英文摘要。

## 论文大纲
{outline}

## 研究结论
{conclusion}

要求：
- 中文摘要 300-500 字
- 英文摘要对应翻译
- 结构：研究目的、方法、结果、结论
- 关键词中英文各 5 个"""
